(function () {
  const KEY = 'tcbt_lang';
  const SUPPORTED = ['vi', 'en'];

  window.getLang = function () {
    try { const s = localStorage.getItem(KEY); if (SUPPORTED.includes(s)) return s; } catch (e) {}
    return null; // chưa chọn
  };
  window.currentLang = window.getLang() || 'vi';

  // lấy giá trị theo khoá
  window.tr = function (key) {
    const dict = window.I18N[window.currentLang] || window.I18N.vi;
    return (dict[key] != null) ? dict[key] : (window.I18N.vi[key] != null ? window.I18N.vi[key] : key);
  };
  // chọn giá trị từ trường song ngữ trong data: {vi,en} -> chuỗi; mảng {vi:[],en:[]} -> mảng
  window.pick = function (v) {
    if (v && typeof v === 'object' && !Array.isArray(v) && ('vi' in v || 'en' in v)) {
      return v[window.currentLang] != null ? v[window.currentLang] : (v.vi != null ? v.vi : v.en);
    }
    return v;
  };

  // áp dụng cho mọi [data-i18n] (đặt innerHTML để cho phép thẻ <span>, <strong>)
  window.applyI18n = function () {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.innerHTML = window.tr(el.getAttribute('data-i18n'));
    });
    const t = window.I18N[window.currentLang]['doc.title'];
    if (t) document.title = t;
    document.documentElement.lang = window.currentLang;
    // cập nhật nhãn nút chuyển ngôn ngữ
    document.querySelectorAll('[data-lang-btn]').forEach(b => {
      b.classList.toggle('on', b.getAttribute('data-lang-btn') === window.currentLang);
    });
  };

  // đổi ngôn ngữ + render lại nội dung động
  window.setLang = function (lang) {
    if (!SUPPORTED.includes(lang)) return;
    window.currentLang = lang;
    try { localStorage.setItem(KEY, lang); } catch (e) {}
    window.applyI18n();
    if (typeof window.renderDynamic === 'function') window.renderDynamic();
  };
})();
